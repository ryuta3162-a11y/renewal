export function getRenewalDetailByPart(part) {
  if (!part) return null;
  const id = part.partId || part.id || "";
  const label = part.partLabel || part.label || "";
  return RENEWAL_MACHINE_DETAILS.find((d) => d.id === id) ||
    RENEWAL_MACHINE_DETAILS.find((d) => d.label === label || d.name === label) ||
    null;
}

export function formatPrice(value) {
  const n = Number(value || 0);
  if (!Number.isFinite(n) || n <= 0) return "—";
  return `¥${n.toLocaleString("ja-JP")}`;
}

export function buildSearchText(detail) {
  if (!detail) return "";
  return [
    detail.category,
    detail.subCategory,
    detail.purpose,
    detail.searchCode,
    detail.brand,
    detail.label,
    detail.name,
    detail.note
  ].filter(Boolean).join(" ").toLowerCase();
}
